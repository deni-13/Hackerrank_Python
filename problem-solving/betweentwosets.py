def getTotalX(a, b):
    counter=0
    for k in range(max(a), min(b)+1):
        flag=True
        for a_e in a:
            if k% a_e!=0:
                flag =False
                break
        for b_el in b:
            if b_el % k!=0:
                flag=False
                break
        if flag== True:
            counter+=1
    return counter
                
        
        
